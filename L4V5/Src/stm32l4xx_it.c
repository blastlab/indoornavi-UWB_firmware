/**
* @brief This function handles RTC wake-up interrupt through EXTI line 20.
*/
void RTC_WKUP_IRQHandler(void)
{
  /* USER CODE BEGIN RTC_WKUP_IRQn 0 */
	Trace(TRACE_WAKE_TIM_ENTER);
	LL_RTC_ClearFlag_WUT(RTC);
	LL_EXTI_ClearFlag_0_31(LL_EXTI_LINE_20);
  /* USER CODE END RTC_WKUP_IRQn 0 */
  /* USER CODE BEGIN RTC_WKUP_IRQn 1 */
	PORT_WatchdogRefresh();
//	PORT_LedOn(LED_ERR);
//	for (volatile int i = 19999; i > 0; --i) {
//		asm("nop");
//	}
	TRANSCEIVER_WakeUp();
	PORT_ExitSleepMode();
	static int i = 0;
	if (i-- == 0) {
		i = 3;
		//PORT_AdcWake();
		PORT_BatteryMeasure();
	}
	SYNC_SendBeacon();
	Trace(TRACE_WAKE_TIM_EXIT);
  /* USER CODE END RTC_WKUP_IRQn 1 */
}

/**
* @brief This function handles EXTI line0 interrupt.
*/
void EXTI0_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI0_IRQn 0 */
	Trace(TRACE_DW_IRQ_ENTER);
	do
  {
    HAL_GPIO_WritePin(DW_CS_GPIO_Port, DW_CS_Pin, GPIO_PIN_SET);
    dwt_isr();
  } while (HAL_GPIO_ReadPin(DW_IRQ_GPIO_Port, DW_IRQ_Pin) == GPIO_PIN_SET);

  /* USER CODE END EXTI0_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_0);
  /* USER CODE BEGIN EXTI0_IRQn 1 */
	Trace(TRACE_DW_IRQ_EXIT);
  /* USER CODE END EXTI0_IRQn 1 */
}

